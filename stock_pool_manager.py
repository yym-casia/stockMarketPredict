    def ensure_pool_size(
        self,
        target: int,
        exclude_st: bool = True,
        min_market_cap: float = 20,
        max_market_cap: float = 5000,
        refresh_days: int = 7,
    ):
        """扩展股票池至 target，基础池优先，其余按市场流动性补齐。"""
        if not self._base_pool_order:
            self._base_pool_order = list(dict.fromkeys(
                s for stocks in self.sector_stocks.values() for s in stocks
            ))
        active = self.get_all_stocks()
        need_refresh = (
            len(active) < target
            or self.target_size != target
            or len(self._screening_order) < target
            or self._pool_is_stale(refresh_days)
        )
        if not need_refresh:
            return

        print(f'扩展股票池至 {target} 只...')
        prev_order = list(self._screening_order)
        market_codes = self._fetch_market_candidates(
            exclude_st=exclude_st,
            min_market_cap=min_market_cap,
            max_market_cap=max_market_cap,
        )
        expired = set(self.EXPIRED_STOCKS.keys())
        screening: List[str] = []
        seen: Set[str] = set()

        for code in self._base_pool_order:
            if code in expired or code in seen:
                continue
            self.stock_pool.add(code)
            screening.append(code)
            seen.add(code)
            if len(screening) >= target:
                break

        if len(screening) < target:
            for code in market_codes:
                if code in expired or code in seen:
                    continue
                self.stock_pool.add(code)
                if code not in self.stock_sectors or self.stock_sectors[code] in ('扩展池', '其他'):
                    try:
                        from src.sector_service import get_eastmoney_sector
                        self.stock_sectors[code] = get_eastmoney_sector(code)
                    except Exception:
                        self.stock_sectors[code] = '其他'
                screening.append(code)
                seen.add(code)
                if len(screening) >= target:
                    break

        if len(screening) < target and len(prev_order) >= len(screening):
            print(f'扩展未达目标({len(screening)}/{target})，保留原选股序列')
            return

        self._screening_order = screening[:target]
        self.target_size = target
        self.last_update = datetime.now()
        self._save_pool()
        print(f'股票池已扩展: 总量 {len(self.stock_pool)} 只, 选股序列 {len(self._screening_order)} 只')

    def get_screening_stocks(self, pool_size: int) -> List[str]:
        """按配置规模返回选股序列（基础池优先）。"""
        active = set(self.get_all_stocks())
        if self._screening_order and len(self._screening_order) >= pool_size:
            return [c for c in self._screening_order if c in active][:pool_size]
        base = [c for c in self._base_pool_order if c in active]
        extra = sorted(active - set(base))
        return (base + extra)[:pool_size]

    def get_all_stocks(self) -> List[str]:
        """获取所有未过期的股票代码（日常显示用）"""
        # 排除已过期股票
        active_stocks = self.stock_pool - set(self.EXPIRED_STOCKS.keys())
        return sorted(list(active_stocks))
    
    def get_all_stocks_with_expired(self) -> List[str]:
        """获取所有股票代码，包括已过期（回测用）"""
        return sorted(list(self.stock_pool))
    
